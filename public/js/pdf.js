// ── Core helpers (private) ───────────────────────────────────
const _P = (() => {
  const GREEN = [20, 90, 60];
  const GRAY  = [244, 245, 247];
  const T2    = [95, 107, 122];

  function cleanText(value) {
    const text = String(value ?? '');
    return text
      .replace(/Ã‚Â·|Â·/g, ' · ')
      .replace(/Ã¢â‚¬â€|â€”/g, '-')
      .replace(/Ã¢â€ â€™|→/g, '->')
      .replace(/Ã¢â€ Â|←/g, '<-')
      .replace(/Ã¢â‚¬Â¦/g, '...')
      .replace(/₹/g, 'Rs ')
      .replace(/\s{2,}/g, ' ')
      .trim();
  }

  function cleanCellValue(value) {
    if (value == null) return '';
    if (typeof value === 'number' || typeof value === 'boolean') return value;
    if (Array.isArray(value)) return value.map(cleanCellValue).join(', ');
    return cleanText(value);
  }

  function init(landscape) {
    const { jsPDF } = window.jspdf;
    return new jsPDF({ orientation: landscape ? 'landscape' : 'portrait', unit: 'mm', format: 'a4' });
  }

  function W(doc) { return doc.internal.pageSize.getWidth(); }
  function H(doc) { return doc.internal.pageSize.getHeight(); }

  function header(doc, title, subtitle) {
    const w = W(doc);
    doc.setFillColor(...GREEN);
    doc.rect(0, 0, w, 22, 'F');
    doc.setTextColor(255, 255, 255);
    doc.setFontSize(7.5); doc.setFont('helvetica', 'bold');
    doc.text('EXPENSE LITE AI', 14, 8);
    doc.setFontSize(13); doc.text(cleanText(title), 14, 17.5);
    if (subtitle) {
      doc.setFontSize(8); doc.setFont('helvetica', 'normal');
      doc.text(cleanText(subtitle), w - 14, 17.5, { align: 'right' });
    }
    doc.setTextColor(0, 0, 0); doc.setFont('helvetica', 'normal');
    return 28;
  }

  function footer(doc) {
    const w = W(doc), h = H(doc);
    const n = doc.internal.getNumberOfPages();
    const localeCode = (window.__currencyPrefs && window.__currencyPrefs.localeCode) || 'en-IN';
    const date = new Date().toLocaleDateString(localeCode, { day: '2-digit', month: 'short', year: 'numeric' });
    for (let i = 1; i <= n; i++) {
      doc.setPage(i);
      doc.setFillColor(237, 240, 243);
      doc.rect(0, h - 9, w, 9, 'F');
      doc.setFontSize(7); doc.setFont('helvetica', 'normal'); doc.setTextColor(...T2);
      doc.text(cleanText(`Generated by Expense Lite AI · ${date}`), 14, h - 3);
      doc.text(`Page ${i} / ${n}`, w - 14, h - 3, { align: 'right' });
    }
    doc.setTextColor(0, 0, 0);
  }

  function table(doc, y, head, body, colStyles, landscape) {
    const safeHead = (head || []).map((row) => (row || []).map(cleanCellValue));
    const safeBody = (body || []).map((row) => (row || []).map(cleanCellValue));
    doc.autoTable({
      startY: y, head: safeHead, body: safeBody,
      theme: 'grid',
      headStyles: { fillColor: GREEN, textColor: [255,255,255], fontStyle: 'bold', fontSize: 8.5, cellPadding: 3 },
      alternateRowStyles: { fillColor: GRAY },
      bodyStyles: { fontSize: 8, cellPadding: 2.5 },
      margin: { left: 14, right: 14, bottom: 14 },
      columnStyles: colStyles || {},
      didDrawPage: (d) => {
        if (d.pageNumber > 1) {
          const pw = doc.internal.pageSize.getWidth();
          doc.setFillColor(...GREEN);
          doc.rect(0, 0, pw, 7, 'F');
          doc.setFontSize(6.5); doc.setFont('helvetica', 'bold'); doc.setTextColor(255,255,255);
          doc.text('EXPENSE LITE AI (cont.)', 14, 5);
          doc.setTextColor(0,0,0); doc.setFont('helvetica','normal');
        }
      }
    });
    return doc.lastAutoTable.finalY + 6;
  }

  function cards(doc, y, items) {
    const w = W(doc), n = items.length, gap = 4;
    const bw = (w - 28 - (n - 1) * gap) / n;
    items.forEach((it, i) => {
      const x = 14 + i * (bw + gap);
      doc.setFillColor(...GRAY);
      doc.roundedRect(x, y, bw, 17, 1.5, 1.5, 'F');
      doc.setFontSize(6.5); doc.setFont('helvetica','normal'); doc.setTextColor(...T2);
      doc.text(cleanText(String(it.label).toUpperCase()), x + 3, y + 5.5);
      const val = cleanText(String(it.value));
      doc.setFontSize(val.length > 16 ? 7.5 : val.length > 11 ? 8.5 : 10.5); doc.setFont('helvetica','bold');
      const c = it.color;
      doc.setTextColor(
        c==='green'?22:c==='red'?185:c==='amber'?180:30,
        c==='green'?163:c==='red'?55:c==='amber'?120:30,
        c==='green'?74:c==='red'?55:c==='amber'?10:30
      );
      doc.text(val, x + 3, y + 13.5, { maxWidth: bw - 6 });
    });
    doc.setTextColor(0,0,0); doc.setFont('helvetica','normal');
    return y + 22;
  }

  function section(doc, y, text) {
    doc.setFontSize(9.5); doc.setFont('helvetica','bold'); doc.setTextColor(...GREEN);
    doc.text(cleanText(text), 14, y);
    doc.setTextColor(0,0,0); doc.setFont('helvetica','normal');
    return y + 6;
  }

  function note(doc, y, text) {
    doc.setFontSize(7.5); doc.setFont('helvetica','normal'); doc.setTextColor(...T2);
    doc.text(cleanText(text), 14, y);
    doc.setTextColor(0,0,0);
    return y + 5;
  }

  function save(doc, name) {
    footer(doc);
    doc.save(name.replace(/[^\w\s\-]/g,'_').trim() + '.pdf');
  }

  function cur(n) {
    if (typeof fmtCur === 'function') return fmtCur(n);
    const v = parseFloat(n)||0, neg = v < 0;
    return (neg?'- INR ':'INR ') + Math.abs(v).toFixed(2);
  }

  function dt(d) {
    if (!d) return '—';
    const localeCode = (window.__currencyPrefs && window.__currencyPrefs.localeCode) || 'en-IN';
    const raw = String(d).trim();
    let dtValue;
    const onlyDate = raw.match(/^(\d{4})-(\d{2})-(\d{2})$/);
    if (onlyDate) dtValue = new Date(Number(onlyDate[1]), Number(onlyDate[2]) - 1, Number(onlyDate[3]));
    else dtValue = new Date(raw);
    if (Number.isNaN(dtValue.getTime())) return raw;
    return dtValue.toLocaleDateString(localeCode,{day:'2-digit',month:'short',year:'numeric'});
  }

  function curSafe(n) {
    const value = Number(n || 0);
    const neg = value < 0;
    const currencyCode = (window.__currencyPrefs && window.__currencyPrefs.currencyCode) || 'INR';
    const localeCode = (window.__currencyPrefs && window.__currencyPrefs.localeCode) || 'en-IN';
    let amountText = Math.abs(value).toFixed(2);
    try {
      amountText = new Intl.NumberFormat(localeCode, {
        minimumFractionDigits: 2,
        maximumFractionDigits: 2,
      }).format(Math.abs(value));
    } catch (_err) {}
    const prefix = currencyCode === 'INR' ? 'Rs ' : `${currencyCode} `;
    return cleanText(`${neg ? '- ' : ''}${prefix}${amountText}`);
  }

  function dtSafe(d) {
    if (!d) return '-';
    const localeCode = (window.__currencyPrefs && window.__currencyPrefs.localeCode) || 'en-IN';
    const raw = String(d).trim();
    let dtValue;
    const onlyDate = raw.match(/^(\d{4})-(\d{2})-(\d{2})$/);
    if (onlyDate) dtValue = new Date(Number(onlyDate[1]), Number(onlyDate[2]) - 1, Number(onlyDate[3]));
    else dtValue = new Date(raw);
    if (Number.isNaN(dtValue.getTime())) return cleanText(raw);
    return cleanText(dtValue.toLocaleDateString(localeCode, { day:'2-digit', month:'short', year:'numeric' }));
  }

  const MONTHS = ['Jan','Feb','Mar','Apr','May','Jun','Jul','Aug','Sep','Oct','Nov','Dec'];

  return { init, W, H, header, footer, table, cards, section, note, save, cur: curSafe, dt: dtSafe, MONTHS };
})();

