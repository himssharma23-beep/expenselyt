  if (loanFilters.type==='paid')     txns=txns.filter(t=>t.paid>0);
  if (loanFilters.type==='received') txns=txns.filter(t=>t.received>0);
  txns.sort((a,b)=>a.txn_date<b.txn_date?1:-1);

  const filterLabel = [
    loanFilters.year  ? `Year: ${loanFilters.year}`  : '',
    loanFilters.month ? `Month: ${loanFilters.month}` : '',
    loanFilters.search? `Search: "${loanFilters.search}"` : '',
    loanFilters.type  ? `Type: ${loanFilters.type}`  : '',
  ].filter(Boolean).join(' · ') || 'All transactions';

  const paidF = txns.reduce((s,t)=>s+(t.paid||0),0);
  const rcvdF = txns.reduce((s,t)=>s+(t.received||0),0);

  const doc = _P.init();
  let y = _P.header(doc, `Transactions — ${f.name}`, filterLabel);
  y = _P.cards(doc, y, [
    { label:'Overall Balance',   value:_P.cur(balance||0),   color:(balance||0)>0?'green':(balance||0)<0?'red':'' },
    { label:'Total Paid/Given',  value:_P.cur(paidF),        color:'red' },
    { label:'Total Received',    value:_P.cur(rcvdF),        color:'green' },
    { label:'Transactions',      value:txns.length,           color:'' },
  ]);

  // Running balance in chronological order
  const chrono = [...txns].reverse();
  let running = 0;
  const runMap = {};
  chrono.forEach(t => { running += (t.paid||0)-(t.received||0); runMap[t.id]=running; });

  _P.table(doc, y,
    [['Date','Details','Paid / Given','Received','Running Net']],
    txns.map(t=>[
      _P.dt(t.txn_date),
      t.details || '—',
      t.paid>0  ? _P.cur(t.paid)     : '—',
      t.received>0 ? _P.cur(t.received) : '—',
      _P.cur(runMap[t.id]||0),
    ]),
    { 0:{cellWidth:28}, 1:{cellWidth:'auto'}, 2:{halign:'right',cellWidth:38}, 3:{halign:'right',cellWidth:38}, 4:{halign:'right',fontStyle:'bold',cellWidth:40} }
  );
  _P.save(doc, `Transactions_${f.name}`);
