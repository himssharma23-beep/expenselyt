// ── Split Session Detail ─────────────────────────────────────
function downloadSplitSessionPdf(sessionKey) {
  const groups = (_divGroups || []).filter(g => (g.session_id || `_solo_${g.id}`) === sessionKey);
  if (!groups.length) return;

  const sess = groups[0];
  const title = sess.heading || sess.details || 'Split';
  const sessionTotal = groups.reduce((s,g)=>s+(g.total_amount||0),0);

  const doc = _P.init(true);
  let y = _P.header(doc, `Split: ${title}`,
    `${_P.dt(sess.divide_date)}  ·  ${groups.length} item${groups.length!==1?'s':''}`);

  y = _P.cards(doc, y, [
    { label:'Items',        value:groups.length,          color:'' },
    { label:'Session Total',value:_P.cur(sessionTotal),  color:'' },
  ]);

  groups.forEach((g, idx) => {
    if (groups.length > 1) y = _P.section(doc, y, `${idx+1}. ${g.details}  —  ${g.paid_by}  ·  ${_P.cur(g.total_amount)}`);

    const splits = g.splits || [];
    const friendsTotal = splits.reduce((s,x)=>s+x.share_amount, 0);
    const myShare = Math.round((g.total_amount - friendsTotal) * 100) / 100;
    const paidByYou = g.paid_by === 'You';
    const allP = [...splits.map(s=>({ name:s.friend_name, share:s.share_amount, isMe:false }))];
    if (myShare > 0.005) allP.unshift({ name:'You (me)', share:myShare, isMe:true });

    y = _P.table(doc, y,
      [['Person','Share','Paid Upfront','Owes / Gets Back']],
      allP.map(p => {
        const isPayer = (p.isMe && paidByYou) || (!p.isMe && g.paid_by === p.name);
        const paidUp = isPayer ? g.total_amount : 0;
        const net = p.share - paidUp;
        const netLabel = Math.abs(net) < 0.005 ? 'Settled'
          : net > 0 ? `Owes ${_P.cur(net)}`
          : `Gets back ${_P.cur(Math.abs(net))}`;
        return [
          p.name + (p.isMe ? ' (me)' : ''),
          _P.cur(p.share),
          isPayer ? _P.cur(paidUp) : '—',
          netLabel,
        ];
      }),
      { 1:{halign:'right',fontStyle:'bold'}, 2:{halign:'right'}, 3:{halign:'right'} }
    );
  });

  _P.save(doc, `Split_${title}`);
}

function tripStartsInPdfLabel(startDate) {
  const raw = String(startDate || '').trim();
  if (!raw) return '';
  const today = new Date();
  const current = new Date(today.getFullYear(), today.getMonth(), today.getDate());
  const normalized = raw.slice(0, 10);
  const start = new Date(`${normalized}T00:00:00`);
  if (Number.isNaN(start.getTime())) return '';
  const diff = Math.round((start.getTime() - current.getTime()) / 86400000);
  if (diff < 0) return '';
  const inclusiveDays = diff + 1;
  if (inclusiveDays <= 0) return '';
  return inclusiveDays === 1 ? 'Starts in 1 day' : `Starts in ${inclusiveDays} days`;
}

function tripItineraryTimeLabel(item) {
  const start = String(item?.start_time || '').trim();
  const end = String(item?.end_time || '').trim();
  if (start && end) return `${start} - ${end}`;
  return start || end || 'Any time';
}

