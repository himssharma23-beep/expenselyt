async function downloadFriendsPdf() {
  const data = await api('/api/friends');
  if (!data) return;
  const friends = data.friends || [];
  const nb = data.netBalance || 0;
  const owed = friends.filter(f=>f.balance>0).reduce((s,f)=>s+f.balance,0);
  const owe  = friends.filter(f=>f.balance<0).reduce((s,f)=>s+Math.abs(f.balance),0);

  const doc = _P.init(true);
  let y = _P.header(doc, 'Friends & Loans Overview',
    `${friends.length} friend${friends.length!==1?'s':''}  ·  ${new Date().toLocaleDateString('en-IN')}`);
  y = _P.cards(doc, y, [
    { label:'Net Balance',   value:_P.cur(nb),    color: nb>0?'green':nb<0?'red':'' },
    { label:'Others Owe Me', value:_P.cur(owed),  color:'green' },
    { label:'I Owe Others',  value:_P.cur(owe),   color:'red' },
    { label:'Total Friends', value:friends.length, color:'' },
  ]);
  _P.table(doc, y,
    [['Friend','Balance','Status']],
    friends.map(f=>[
      f.name,
      _P.cur(f.balance),
      f.balance>0.005?'They owe me':f.balance<-0.005?'I owe':'Settled'
    ]),
    { 0:{cellWidth:80}, 1:{halign:'right',fontStyle:'bold',cellWidth:70}, 2:{halign:'center',cellWidth:32} }
  );
  _P.save(doc, 'Friends_Overview');
}

